<h1>Le BUT GEII à CALAIS concrètement comment ça se passe - El HABIB et DIALLO</h1>

<p>
Le BUT GEII à CALAIS concrètement comment ça se passe : 3 ans professionnalisants, approche par compétence, alternance possible
</p>

<h2>Le déroulement en 3 ans de la formation :</h2>

<p>
La formation s’appuie sur une pédagogie progressive mêlant cours magistraux pour acquérir les bases théoriques, travaux dirigés et pratiques pour appliquer concrètement les connaissances, ainsi que des études, réalisations et projets menés tout au long des trois années.
</p>

<p>
Ces activités permettent de passer de la théorie à la conception et à la réalisation de systèmes fonctionnels, de travailler en équipe, de développer l’autonomie, la transversalité des savoirs et l’approfondissement des compétences. Deux stages, représentant 22 à 26 semaines, complètent le parcours en offrant une immersion dans le monde professionnel et l’occasion de mettre en pratique, renforcer et enrichir les compétences acquises.
</p>

<h2>Les parcours de professionnalisation :</h2>

<p>
En plus du tronc commun qui s’étale sur les 3 ans de la formation, dès la deuxième année, l’étudiant devra choisir un parcours en adéquation avec ses envies, ses compétences et selon les parcours proposés dans le département GEII où il étudie.
</p>

<h3>Automatisme et Informatique Industrielle</h3>

<p>
Ce parcours forme à l’automatisme, à la robotique et à la programmation de systèmes industriels. Il prépare à l’installation, au pilotage et à la supervision de procédés automatisés, en intégrant les technologies clés de l’industrie du futur telles que l’IoT, le jumeau numérique, l’IA, le Cloud, le Big Data et la cybersécurité.
</p>

<h3>Électronique et Systèmes Embarqués</h3>

<p>
Ce parcours développe des compétences en conception, intégration et maintenance de systèmes électroniques et embarqués. Il prépare à travailler en équipe avec des techniciens et des ingénieurs sur des applications variées (domotique, robotique, transports, santé, IoT, IA), en maîtrisant les systèmes de communication hertziens et optiques.
</p>

<h2>Les modules enseignes au cours de cette formation :</h2>

<p>
Les enseignements cœur de métier couvrent les fondamentaux de l’électricité et de l’électronique, de l’automatique, de l’énergie et de l’électrotechnique, de l’automatisme et de l’informatique industrielle. Ils permettent d’acquérir les compétences nécessaires à l’analyse, à la conception et à la mise en œuvre de systèmes industriels modernes, intégrant automatisation, robotique, réseaux industriels, électronique de puissance et informatique de l’industrie 4.0.
</p>

<p>
Les enseignements support développent les compétences transversales à travers l’étude et réalisation et les projets, favorisant l’autonomie, le travail en équipe et la gestion de projet. Ils incluent également les fondamentaux humains et scientifiques, renforçant la communication, l’anglais technique, les mathématiques et la construction du parcours professionnel.
</p>

<h2>L’évaluation des enseignants :</h2>

<p>
Au BUT GEII, l’évaluation se fait principalement par compétence. Chaque étudiant doit valider trois compétences communes et une compétence spécifique à son parcours, chacune déclinée en plusieurs niveaux. Des mises en situation professionnelles permettent de démontrer les acquis, tandis qu’un portfolio documente les activités réalisées et sert à présenter le niveau de compétence atteint devant un jury.
</p>

<h2>Les compétences en communs :</h2>

<ul>
    <li>Concevoir : concevoir la partie d’un système.</li>
    <li>Vérifier : vérifier la partie d’un système.</li>
    <li>Maintenir : assurer le maintien en condition opérationnelle d'un système.</li>
</ul>

<h2>Les possibilité d’alternance :</h2>

<p>
Le BUT GEII peut être suivi en alternance, mais seulement dans les IUT qui proposent cette formule, ce qui nécessite de bien se renseigner avant de candidater. L’alternance permet d’alterner périodes en entreprise et périodes de formation à l’IUT, offrant une immersion progressive dans le monde professionnel tout en poursuivant les enseignements théoriques et pratiques. Elle peut se réaliser sous contrat d’apprentissage ou contrat de professionnalisation, selon les modalités choisies par l’établissement.
</p>

<p>
L’organisation du temps est adaptée : le volume horaire à l’IUT est réduit d’environ 15 % en première année, 25 % en deuxième année et 20 % en troisième année, permettant à l’étudiant de consacrer une part importante de son temps à l’expérience professionnelle et à l’acquisition de compétences directement applicables en entreprise.
</p>

   
